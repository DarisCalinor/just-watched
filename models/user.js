//  =====================================================
//  KICK-OFF  ===========================================
//  =====================================================
var
	mongoose              = require("mongoose"),
	passportLocalMongoose = require("passport-local-mongoose");

//  =====================================================
//  USER MODEL  =========================================
//  =====================================================
var userSchema = new mongoose.Schema({
	username: String,
	password: String
});

//  =====================================================
//  Plugin Utilization  =================================
//  =====================================================
userSchema.plugin(passportLocalMongoose);

//  =====================================================
//  EXPORT  =============================================
//  =====================================================
module.exports = mongoose.model("User", userSchema);